class Sample5 {
	public static void main(String[] args) {
		// 処理を記述;
		
		// 数値（int）型の変数（箱）として、numを宣言
		int num;

		// 数値の代入
		num = 28;       // 左辺 ← 右辺 となる
		num = num * 2;  // num ← num x 2 となる

		System.out.println(num);

		// おまけ
		System.out.println("num");   //とすると・・・
		// 「num」という文字が出てきてしまう


	}
}